 	private Map<String,Object> populationMap(StreetEntity t){
		Map<String,Object> map = new HashMap<String,Object>();
		map.put("streetid", t.getStreetid());
		map.put("streetname", t.getStreetname());
		map.put("streetcode", t.getStreetcode());
		map.put("townid", t.getTownid());
		return map;
	}
 	
 	/**
	 * 替换sql中的变量
	 * @param sql
	 * @param t
	 * @return
	 */
 	public String replaceVal(String sql,StreetEntity t){
 		sql  = sql.replace("#{streetid}",String.valueOf(t.getStreetid()));
 		sql  = sql.replace("#{streetname}",String.valueOf(t.getStreetname()));
 		sql  = sql.replace("#{streetcode}",String.valueOf(t.getStreetcode()));
 		sql  = sql.replace("#{townid}",String.valueOf(t.getTownid()));
 		sql  = sql.replace("#{UUID}",UUID.randomUUID().toString());
 		return sql;
 	}
 	
 	/**
	 * 执行JAVA增强
	 */
 	private void executeJavaExtend(String cgJavaType,String cgJavaValue,Map<String,Object> data) throws Exception {
 		if(StringUtil.isNotEmpty(cgJavaValue)){
			Object obj = null;
			try {
				if("class".equals(cgJavaType)){
					//因新增时已经校验了实例化是否可以成功，所以这块就不需要再做一次判断
					obj = MyClassLoader.getClassByScn(cgJavaValue).newInstance();
				}else if("spring".equals(cgJavaType)){
					obj = ApplicationContextUtil.getContext().getBean(cgJavaValue);
				}
				if(obj instanceof CgformEnhanceJavaInter){
					CgformEnhanceJavaInter javaInter = (CgformEnhanceJavaInter) obj;
					javaInter.execute("street",data);
				}
			} catch (Exception e) {
				e.printStackTrace();
				throw new Exception("执行JAVA增强出现异常！");
			} 
		}
 	}
